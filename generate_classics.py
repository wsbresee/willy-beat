def safe_filename(s: str) -> str:
    return re.sub(r'[/\\:*?"<>|]', "_", s).strip() or "classic"


def compute_density(rows):
    total = 10 * 16
    active = sum(1 for r in rows for ch in r if ch in "gsmha")
    return active / total if total else 0.0


def format_beat(name: str, genres: str, rows) -> str:
    density = compute_density(rows)
    lines = [
        "# WillyBeat Preset",
        f"name:    {name}",
        f"genres:  {genres}",
        "type:    Regular",
        f"density: {density:.4f}",
        "",
        "# velocity codes:  . off   g ghost(25)   s soft(55)   m medium(80)   h hard(100)   a accent(120)",
    ]
    for key, row in zip(TRACK_KEYS, rows):
        lines.append(f"{key:<8}{row}")
    lines.append("")
    return "\n".join(lines)


def main():
    PRESETS_DIR.mkdir(parents=True, exist_ok=True)
    written = 0
    for entry in CLASSICS:
        name, genres, *rows = entry
        text = format_beat(name, genres, rows)
        path = PRESETS_DIR / f"{safe_filename(name)}.beat"
        path.write_text(text, encoding="utf-8")
        written += 1
    print(f"Wrote {written} classic presets -> {PRESETS_DIR}")


if __name__ == "__main__":
    main()
