# ─── Write to .beat files ────────────────────────────────────────────────────


def safe_filename(s: str) -> str:
    return re.sub(r'[/\\:*?"<>|]', "_", s).strip() or "fill"


def compute_density(rows):
    total = 10 * 16
    active = sum(1 for r in rows for ch in r if ch in "gsmha")
    return active / total if total else 0.0


def format_beat(name: str, genres: str, pat_type: str, rows) -> str:
    density = compute_density(rows)
    lines = [
        "# WillyBeat Preset",
        f"name:    {name}",
        f"genres:  {genres}",
        f"type:    {pat_type}",
        f"density: {density:.4f}",
        "",
        "# velocity codes:  . off   g ghost(25)   s soft(55)   m medium(80)   h hard(100)   a accent(120)",
    ]
    for ti, key in enumerate(TRACK_KEYS):
        lines.append(f"{key.ljust(8)}{rows[ti]}")
    return "\n".join(lines) + "\n"


def main():
    PRESETS_DIR.mkdir(parents=True, exist_ok=True)
    written = 0
    bad_lengths = []

    for fill in FILLS:
        name, genres, pat_type, *rows = fill
        if len(rows) != 10:
            print(f"  ✗ {name}: expected 10 rows, got {len(rows)}")
            continue
        for i, r in enumerate(rows):
            if len(r) != 16:
                bad_lengths.append((name, TRACK_KEYS[i], len(r)))

        text = format_beat(name, genres, pat_type, rows)
        out = PRESETS_DIR / (safe_filename(name) + ".beat")
        out.write_text(text)
        written += 1

    print(f"Wrote {written} fill presets → {PRESETS_DIR}")
    if bad_lengths:
        print(f"\n  Warning: {len(bad_lengths)} rows are not exactly 16 chars:")
        for name, track, n in bad_lengths[:20]:
            print(f"    {name} / {track}: {n} chars")


if __name__ == "__main__":
    main()
